def chFromBitMap(a,b,c,d):
           # 0x00 0x01 0x02 0x03
    quad = [ ' ', '▘', '▝', '▀',
           # 0x04 0x05 0x06 0x07
             '▖', '▌', '▞', '▛',
           # 0x08 0x09 0x0A 0x0B
             '▗', '▚', '▐', '▜',
           # 0x0C 0x0D 0x0E 0x0F
             '▄', '▙', '▟', '█']
    return quad[0x01*a+0x02*b+0x04*c+0x08*d]

